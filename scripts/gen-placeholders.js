/**
 * Generates tiny base64 blur placeholders for all product images
 * and writes them to data/placeholders.ts.
 * Also re-compresses images at quality 72 / max 800px.
 */
const sharp = require("sharp");
const fs = require("fs");
const path = require("path");

const PROJECT_ROOT = path.join(__dirname, "..");
process.chdir(PROJECT_ROOT);
sharp.cache(false);

const PRODUCTS_DIR = path.join("public", "products");
const OUT_FILE = path.join("data", "placeholders.ts");
const MAX_W = 800;
const QUALITY = 72;

function walk(dir, results = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, results);
    else if (/\.(webp|jpg|jpeg|png)$/i.test(entry.name)) results.push(full);
  }
  return results;
}

(async () => {
  const files = walk(PRODUCTS_DIR);
  console.log(`Processing ${files.length} images...\n`);

  const placeholders = {};

  for (const filePath of files) {
    const inputBuf = fs.readFileSync(filePath);
    const original = inputBuf.length;

    // Re-compress
    const meta = await sharp(inputBuf).metadata();
    let pipeline = sharp(inputBuf).rotate();
    if (meta.width && meta.width > MAX_W) {
      pipeline = pipeline.resize(MAX_W, null, { withoutEnlargement: true });
    }
    const compressed = await pipeline.webp({ quality: QUALITY, effort: 6 }).toBuffer();

    if (compressed.length < original) {
      fs.writeFileSync(filePath, compressed);
      console.log(`  ✓ ${filePath.replace(/\\/g, "/")}  ${(original/1024).toFixed(0)}KB → ${(compressed.length/1024).toFixed(0)}KB`);
    } else {
      console.log(`  ~ ${filePath.replace(/\\/g, "/")}  already optimal (${(original/1024).toFixed(0)}KB)`);
    }

    // Generate blur placeholder (10×10 px, quality 20)
    const blurBuf = await sharp(compressed.length < original ? compressed : inputBuf)
      .resize(10, 10, { fit: "cover" })
      .webp({ quality: 20 })
      .toBuffer();

    // URL key matches what the catalog uses: /products/...
    const urlKey = "/" + filePath.replace(/\\/g, "/").replace(/^public\//, "");
    placeholders[urlKey] = `data:image/webp;base64,${blurBuf.toString("base64")}`;
  }

  const lines = Object.entries(placeholders)
    .map(([k, v]) => `  "${k}": "${v}",`)
    .join("\n");

  const output = `// Auto-generated by scripts/gen-placeholders.js — do not edit manually
export const IMAGE_PLACEHOLDERS: Record<string, string> = {
${lines}
};
`;

  fs.writeFileSync(OUT_FILE, output);
  console.log(`\n✓ Written ${Object.keys(placeholders).length} placeholders to ${OUT_FILE}`);
})();
